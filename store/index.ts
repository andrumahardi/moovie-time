type Movie = {
  id: number;
  title: string;
  imgUrl: string;
  rating: string;
  year: number;
  genre: string;
  desc: string;
  link: string;
  isRecommended: boolean;
};

type CarouselItem = Omit<Movie, "link" | "isRecommended" | "overview">

type Review = {
  id: number;
  reviewer: string;
  date: string;
  rating: string;
  review: string;
}

type State = {
  movies: Movie[];
  carouselItems: CarouselItem[];
  reviews: Review[];
};

export const getters = {
  getMovies: (state: State) => (num: number) => {
    return state.movies.slice(0, num);
  },
  getMovieBySlug: (state: State) => (slug: string) => {
    return state.movies.find(
      (mov) => mov.link.toLowerCase() === `/movies/${slug.toLowerCase()}`
    );
  },
  getRecommendedMovies: (state: State) => {
    return state.movies.filter(
      (mov) => mov.isRecommended
    );
  }
};
